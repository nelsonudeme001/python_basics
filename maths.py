import math

pi = -3.142
g = 10.015
a, b, c = 2, 3, 4

# (in-built fxn) rounds to the nearest whole integer
print(round(pi))
print(abs(pi))
print(pow(pi, 2))
print(max(a, b, c))
print(min(a, b, c))

# maths functions in the math python package
print(math.ceil(pi))
print(math.floor(pi))
print(math.sqrt(g))
